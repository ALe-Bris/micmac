template <class T,class TB>
double VarLapl(Im2D<T,TB> * aIm,int aSzW);
//burriness is computed as variance of  Laplacian with a mean filter prior to reduce noise
//https://www.pyimagesearch.com/2015/09/07/blur-detection-with-opencv/


template <class T,class TB>
double VarLapl(Im2D<T,TB> * aIm,int aSzW)
{
   Im2D_REAL4 aRes(aIm->sz().x,aIm->sz().y);

   Fonc_Num aF = aIm->in_proj();

   int aNbVois = ElSquare(1+2*aSzW);

   aF = rect_som(aF,aSzW) /aNbVois;
   ELISE_COPY(aIm->all_pts()
              ,aF
              ,aRes.out());

   double mean;
   int nb;

   ELISE_COPY(aRes.all_pts()
              ,Virgule(Laplacien(aRes.in_proj())                           ,1)
              ,Virgule(aRes.out()|sigma(mean)      ,sigma(nb)));

   std::cout << "mean of laplacian : " << mean << "\n";
   mean=mean/nb;

   std::cout << "nb of value: " << nb << "\n";
   // variance of laplacian
   Fonc_Num aFVar = ElSquare((aRes.in()-mean));

   double meanVarLap;
   ELISE_COPY(aRes.all_pts()
              ,aFVar
              ,sigma(meanVarLap));
   // mean of variance
   meanVarLap/=nb;
   return meanVarLap;
}



int ascii2tif(int argc,char ** argv)
{
    std::string aPat("Ree*.tif");
    std::list<std::string> mLFile;
    bool aVario(true);
    Pt2di aSz(1024,768);
    int aHLine(0);
    bool aC2K(false), aDebug(0), aOptris(0), aInt2Deg(0);


    ElInitArgMain
            (
                argc,argv,
                LArgMain()  << EAMC(aPat,"ascii image pattern, images generated either by irbis software (variocam hd thermal camera) or with SDKdirect optris library",eSAM_IsPatFile),
              LArgMain()
                << EAM(aVario,"Vario", true, "Are ascii generated by irbis soft for camera Variocam? Def true.")
                 << EAM(aC2K,"C2K", true, "Variocam: conversion from cercius value to kelvin value *100, in order to got integer value instead of float.")
                 << EAM(aSz,"Sz", true, "Image size, default [1024,768] which is the size of variocam thermal imagery")
                << EAM(aOptris,"Optris", true, "Are ascii generated from optris camera? Def false. if true, set proper image size.")
                << EAM(aInt2Deg,"Int2Deg", true, "Optris: conversion of unsigned integer value (raw data) to degree?.")
                << EAM(aHLine,"DropHL", true, "Number of Line to drop in the Header, default 10 (variocam ascii irbis soft)")
                << EAM(aDebug,"Debug", true, "Display much more info in order to help debugging process")
                );

    if (aVario & !EAMIsInit(&aC2K)) aC2K=true;
    if (aVario & !EAMIsInit(&aHLine)) aHLine=10;

    if (aOptris & !EAMIsInit(&aSz)) aSz=Pt2di(640,480);
    if (aVario & !EAMIsInit(&aInt2Deg)) aInt2Deg=true;

    cInterfChantierNameManipulateur * aICNM = cInterfChantierNameManipulateur::BasicAlloc("./");
    // create the list of images starting from the regular expression (Pattern)
    mLFile = aICNM->StdGetListOfFile(aPat);

    std::cout << mLFile.size() << " ascii images detected\n";

    for (auto & aName : mLFile){

    std::cout << "Ascii to tif, image  " <<aName <<"\n";
    // crée une image Ram de taille adéquate

    int SzX(aSz.x), SzY(aSz.y);
    Im2D_REAL4 aImRAM(SzX,SzY);

    // lecture du fichier ascii
    ifstream ascii(aName);
    std::string output;
    double aVal(0);
    // passe l'entête
    if (aHLine>0) for (int i(0); i<(aHLine-1);i++){ ascii >> output;}

    for (int l(0); l < SzY; l++)
    {
        for (int col(0); col < SzX; col++)
        {
           Pt2di aPos(col,l);
           ascii >> output;
           if (aVario) output[2]='.';
           FromString(aVal,output);

           if (aDebug)
           {
           if (col==0 | col==SzX-1) std::cout << "Valeur lue: " << aVal <<" , position " << aPos <<  "\n";
           }

           aImRAM.SetR(aPos,aVal);
        }
    }

    if (aVario & aC2K)
    {

        std::cout << "conversion celcius vers Kelvin et multiplication par 100.\n";
        ELISE_COPY
                (
                    aImRAM.all_pts(),
                    100*(273.15+aImRAM.in()),
                    aImRAM.out()
                    );

    }

    if (aOptris & aInt2Deg)
    {

        std::cout << "conversion unsigned int Optris PI to degree value.\n";
        ELISE_COPY
                (
                    aImRAM.all_pts(),
                    (aImRAM.in()-1000)/10,
                    aImRAM.out()
                    );

    }

    std::cout << "bluriness of this images: " << VarLapl(&aImRAM,3) << "\n";

    std::string aNameTiff=aName.substr(0, aName.size()-3) + "tif";
    std::cout << "Ecriture de  " <<aNameTiff <<"\n";
    // sauve l'image

    if(aC2K | aInt2Deg) {
    Tiff_Im aTiff
             (
                 aNameTiff.c_str(),
                 aImRAM.sz(),
                 GenIm::real4,
                 Tiff_Im::No_Compr,
                 Tiff_Im::BlackIsZero
             );

    ELISE_COPY
            (
                aImRAM.all_pts(),
                aImRAM.in(),
                aTiff.out()
                );
    } else {
        Tiff_Im aTiff
                 (
                     aNameTiff.c_str(),
                     aImRAM.sz(),
                     GenIm::int2,
                     Tiff_Im::No_Compr,
                     Tiff_Im::BlackIsZero
                 );

        ELISE_COPY
                (
                    aImRAM.all_pts(),
                    aImRAM.in(),
                    aTiff.out()
                    );

    }


    }

    return EXIT_SUCCESS;
}




int main_ascii2tif(int argc,char ** argv)
{
   ascii2tif(argc,argv);
   return EXIT_SUCCESS;
}



